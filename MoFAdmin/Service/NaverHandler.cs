		protected override async Task<HandleRequestResult> HandleRemoteAuthenticateAsync()
		{
			var query = Request.Query;

			var state = query["state"];
			var properties = Options.StateDataFormat.Unprotect(state);

			var error = query["error"];
			if (!StringValues.IsNullOrEmpty(error))
			{
				var failureMessage = new StringBuilder();
				failureMessage.Append(error);
				var errorDescription = query["error_description"];
				if (!StringValues.IsNullOrEmpty(errorDescription))
				{
					failureMessage.Append(";Description=").Append(errorDescription);
				}
				var errorUri = query["error_uri"];
				if (!StringValues.IsNullOrEmpty(errorUri))
				{
					failureMessage.Append(";Uri=").Append(errorUri);
				}

				return HandleRequestResult.Fail(failureMessage.ToString());
			}

			var code = query["code"];

			if (StringValues.IsNullOrEmpty(code))
			{
				return HandleRequestResult.Fail("Code was not found.");
			}

			var context = new OAuthCodeExchangeContext(properties, code, BuildRedirectUri(Options.CallbackPath));
			var tokens = await ExchangeCodeAsync(context);

			if (tokens.Error != null)
			{
				return HandleRequestResult.Fail(tokens.Error);
			}

			if (string.IsNullOrEmpty(tokens.AccessToken))
			{
				return HandleRequestResult.Fail("Failed to retrieve access token.");
			}

			var identity = new ClaimsIdentity(ClaimsIssuer);

			if (Options.SaveTokens)
			{
				var authTokens = new List<AuthenticationToken>();

				authTokens.Add(new AuthenticationToken { Name = "access_token", Value = tokens.AccessToken });
				if (!string.IsNullOrEmpty(tokens.RefreshToken))
				{
					authTokens.Add(new AuthenticationToken { Name = "refresh_token", Value = tokens.RefreshToken });
				}

				if (!string.IsNullOrEmpty(tokens.TokenType))
				{
					authTokens.Add(new AuthenticationToken { Name = "token_type", Value = tokens.TokenType });
				}

				if (!string.IsNullOrEmpty(tokens.ExpiresIn))
				{
					int value;
					if (int.TryParse(tokens.ExpiresIn, NumberStyles.Integer, CultureInfo.InvariantCulture, out value))
					{
						// https://www.w3.org/TR/xmlschema-2/#dateTime
						// https://msdn.microsoft.com/en-us/library/az4se3k1(v=vs.110).aspx
						var expiresAt = Clock.UtcNow + TimeSpan.FromSeconds(value);
						authTokens.Add(new AuthenticationToken
						{
							Name = "expires_at",
							Value = expiresAt.ToString("o", CultureInfo.InvariantCulture)
						});
					}
				}

				properties.StoreTokens(authTokens);
			}

			var ticket = await CreateTicketAsync(identity, properties, tokens);
			return ticket != null ? HandleRequestResult.Success(ticket) : HandleRequestResult.Fail("Failed to retrieve user information from remote server.");
		}

		//protected override async Task<OAuthTokenResponse> ExchangeCodeAsync(string code, string redirectUri)
		protected override async Task<OAuthTokenResponse> ExchangeCodeAsync(OAuthCodeExchangeContext context)
		{
			var tokenRequestParameters = new Dictionary<string, string>()
				{
					 { "client_id", Options.ClientId },
					 { "redirect_uri", context.RedirectUri },
					 { "client_secret", Options.ClientSecret },
					 { "code", context.Code },
					 { "grant_type", "authorization_code" },
				};

			var requestContent = new FormUrlEncodedContent(tokenRequestParameters);

			var requestMessage = new HttpRequestMessage(HttpMethod.Post, Options.TokenEndpoint);
			requestMessage.Headers.Accept.Add(new MediaTypeWithQualityHeaderValue("application/json"));
			requestMessage.Content = requestContent;

			var response = await Backchannel.SendAsync(requestMessage, Context.RequestAborted);

			if (response.IsSuccessStatusCode)
			{
				//이부분을 추가해주지 않으면 euc-kr 인코딩을 지원하지 않는다며 에러가 발생함.
				//Naver만 해당..
				var result = await response.Content.ReadAsByteArrayAsync();
				var payload = JsonDocument.Parse(Encoding.UTF8.GetString(result));
				return OAuthTokenResponse.Success(payload);
			}

			const string error = "OAuth token endpoint failure: ";
			return OAuthTokenResponse.Failed(new Exception(error));
		}

